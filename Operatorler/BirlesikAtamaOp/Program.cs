// 3) *= : Bu operatör mevcut değerle yeni değeri çarpar ve sonucu değişkene atar.

int d = 3;
d *= 2;
Console.WriteLine(d);

// 4) /= : Bu operatör mevcut değeri belirtilen değere böler ve sonucu değişkene atar

int e = 4;
e /= 2; // e=e/2;
Console.WriteLine(e);

//5) %= : Bu operator mevcut değeri belirtilen değere böler ve kalanı sonucu değişkene atar.

int f = 5;
f %= 2; //f=f%2;
Console.WriteLine(f);


