// 1) -= : Bu operatör mevcut değerden belirtilen değeri çıkarır ve sonucu değişkene atar


int a = 5;
a -= 2; //a=a-2;
Console.WriteLine(a);

// 2) += : Bu operatör mevcut değerden belirtilen değere ekler ve sonucu ekrana yazar

int b = 7;
b += 2; //b=b+2;
Console.WriteLine(b);


